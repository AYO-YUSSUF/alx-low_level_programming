#include "main.h"

/**
 * print_array - output elements of an array
 * @n: elements parameter
 * @a: string parameter
 * Return: Anything
*/

void print_array(int *a, int n)
{
	int n;

	for (n = 0; n < n; n++)
	{
		if (n != (n - 1))
			printf("%d, ", a[n]);
		else
			printf("%d, ", a[n]);
	}
	printf("\n");
}
